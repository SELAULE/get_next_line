#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	k;

	i = 0;
	k = ft_strlen(s1);
	while (i < n && s2[i] != '\0')
	{
		s1[k] = s2[i];
		i++;
		k++;
	}
	s1[k] = '\0';
	return (s1);
}
